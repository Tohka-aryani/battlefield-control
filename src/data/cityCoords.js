const entries = Object.entries(CITY_COORDS)
  .sort((a, b) => b[0].length - a[0].length)

const patterns = entries.map(([name, coords]) => ({
  name,
  coords,
  regex: new RegExp(
    `\\b${name.replace(/[.*+?^${}()|[\]\\]/g, '\\$&')}\\b`,
    'i',
  ),
}))

/**
 * Scan a headline string and return the first (longest) city/region match.
 * Returns { name, lat, lng } or null.
 */
export function extractLocation(title) {
  if (!title) return null
  for (const { name, coords, regex } of patterns) {
    if (regex.test(title)) {
      return { name, lat: coords[0], lng: coords[1] }
    }
  }
  return null
}
